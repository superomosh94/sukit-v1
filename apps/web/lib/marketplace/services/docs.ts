export async function saveVideo(
  moduleIdOrSlug: string,
  data: { url: string; title?: string }
) {
  // Videos can be stored in metadata
  const mod = await getModule(moduleIdOrSlug);
  if (!mod) throw new Error('Module not found');
  const meta = (mod.metadata as any) || {};
  const videos = meta.videos || [];
  videos.push(data);
  await prisma.marketplaceModule.update({
    where: { id: mod.id },
    data: { metadata: { ...meta, videos } },
  });
  return { id: data.url, url: data.url, type: 'video' };
}

export async function updateSection(
  moduleIdOrSlug: string,
  sectionType: string,
  data: { title?: string; content: string }
) {
  const docs = await getDocs(moduleIdOrSlug);
  const idx = docs.findIndex((d: any) => d.type === sectionType);
  if (idx === -1) throw new Error('Section not found');
  docs[idx] = { ...docs[idx], ...data };
  const combined = docs.map((d: any) => d.content).join('\n\n---\n\n');
  return updateDocs(moduleIdOrSlug, combined);
}

export async function renderMarkdown(content: string) {
  // Simple markdown to HTML renderer
  const html = content
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/^### (.*?)$/gm, '<h3>$1</h3>')
    .replace(/^## (.*?)$/gm, '<h2>$1</h2>')
    .replace(/^# (.*?)$/gm, '<h1>$1</h1>')
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(/\*(.*?)\*/g, '<em>$1</em>')
    .replace(/`([^`]+)`/g, '<code>$1</code>')
    .replace(
      /```(\w*)\n([\s\S]*?)```/g,
      '<pre><code class="language-$1">$2</code></pre>'
    )
    .replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2">$1</a>')
    .replace(/\n/g, '<br>');
  const toc: { level: number; text: string; id: string }[] = [];
  let m;
  const re = /<h([1-3])>(.*?)<\/h[1-3]>/g;
  while ((m = re.exec(html)) !== null) {
    const id = m[2]
      .toLowerCase()
      .replace(/\s+/g, '-')
      .replace(/[^a-z0-9-]/g, '');
    toc.push({ level: parseInt(m[1], 10), text: m[2], id });
  }
  return { html, toc };
}

export async function uploadImage(buffer: Buffer, _filename: string) {
  const { getStorage } = await import('@/lib/marketplace/storage');
  const { url } = await getStorage().save(
    `docs/images/${Date.now()}-${Math.random().toString(36).slice(2, 8)}`,
    buffer
  );
  return { url };
}

export async function previewDocs(moduleIdOrSlug: string) {
  const docs = await getDocs(moduleIdOrSlug);
  const rendered = await renderMarkdown(
    docs.map((d: any) => d.content).join('\n\n---\n\n')
  );
  return {
    rendered: rendered.html,
    sections: docs.map((d: any) => ({
      type: d.type,
      title: d.title,
      wordCount: d.content.split(/\s+/).length,
    })),
  };
}

export async function exportDocs(
  moduleIdOrSlug: string,
  format: string = 'md'
) {
  const docs = await getDocs(moduleIdOrSlug);
  if (format === 'md') {
    return docs.map((d: any) => d.content).join('\n\n---\n\n');
  }
  if (format === 'html') {
    const rendered = await renderMarkdown(
      docs.map((d: any) => d.content).join('\n\n---\n\n')
    );
    return `<html><head><meta charset="utf-8"></head><body>${rendered.html}</body></html>`;
  }
  if (format === 'json') {
    return JSON.stringify(docs, null, 2);
  }
  throw new Error('Unsupported format');
}
