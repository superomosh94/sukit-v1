function buildFaq(mod: any): {
  faq: { q: string; a: string }[];
  section: DocSection;
} {
  const faq = [
    {
      q: `How do I install ${mod.name}?`,
      a: `Navigate to the Marketplace, search for "${mod.name}", and click Install.`,
    },
    {
      q: `Is ${mod.name} free?`,
      a:
        mod.priceModel === 'free'
          ? 'Yes, this module is free to use.'
          : `This is a commercial module priced at $${mod.price}.`,
    },
    {
      q: 'What permissions does this module require?',
      a: `Required permissions: ${(mod.permissions || []).join(', ') || 'None'}.`,
    },
    {
      q: 'How do I get support?',
      a: mod.supportUrl
        ? `Visit ${mod.supportUrl} for support.`
        : 'Open a support ticket in the marketplace.',
    },
  ];
  return {
    faq,
    section: {
      type: 'faq',
      title: 'FAQ',
      content:
        '## Frequently Asked Questions\n\n' +
        faq.map((f) => `### ${f.q}\n\n${f.a}`).join('\n\n'),
      order: 4,
      autoGenerated: true,
    },
  };
}

