export async function generateDocs(moduleIdOrSlug: string) {
  const mod = await getModule(moduleIdOrSlug);
  if (!mod) throw new Error('Module not found');
  const sections: DocSection[] = [
    buildReadme(mod),
    buildInstallation(mod),
    buildConfiguration(mod),
    buildApi(mod),
    buildFaq(mod).section,
    buildTroubleshooting(mod),
  ];
  const wordCount = sections.reduce(
    (s, sec) => s + sec.content.split(/\s+/).length,
    0
  );
  await prisma.marketplaceModule.update({
    where: { id: mod.id },
    data: { documentation: sections.map((s) => s.content).join('\n\n---\n\n') },
  });
  return {
    moduleId: mod.moduleId,
    sections,
    tableOfContents: sections.map((s) => ({ title: s.title, slug: s.type })),
    wordCount,
    generatedAt: new Date().toISOString(),
  };
}

export async function getDocs(moduleIdOrSlug: string) {
  const mod = await getModule(moduleIdOrSlug);
  if (!mod) return [];
  if (!mod.documentation) {
    const generated = await generateDocs(moduleIdOrSlug);
    return generated.sections;
  }
  // Split documentation into sections
  return mod.documentation.split(/\n\n---\n\n/).map((content, i) => ({
    type:
      [
        'readme',
        'installation',
        'configuration',
        'api',
        'faq',
        'troubleshooting',
      ][i] || 'custom',
    title:
      [
        'README',
        'Installation',
        'Configuration',
        'API Reference',
        'FAQ',
        'Troubleshooting',
      ][i] || 'Custom',
    content,
    order: i,
    autoGenerated: true,
  }));
}

export async function updateDocs(moduleIdOrSlug: string, content: string) {
  const mod = await getModule(moduleIdOrSlug);
  if (!mod) throw new Error('Module not found');
  await prisma.marketplaceModule.update({
    where: { id: mod.id },
    data: { documentation: content },
  });
  return { success: true };
}

export async function saveScreenshot(
  moduleIdOrSlug: string,
  data: {
    url: string;
    thumbnailUrl?: string;
    width?: number;
    height?: number;
    fileSize?: number;
  }
) {
  const mod = await getModule(moduleIdOrSlug);
  if (!mod) throw new Error('Module not found');
  const screenshots = [...(mod.screenshots || []), data.url];
  await prisma.marketplaceModule.update({
    where: { id: mod.id },
    data: { screenshots },
  });
  return {
    id: data.url,
    url: data.url,
    thumbnailUrl: data.thumbnailUrl,
    width: data.width,
    height: data.height,
    fileSize: data.fileSize,
    type: 'image',
  };
}

